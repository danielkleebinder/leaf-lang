package org.leaflang.parser.ast

/**
 * Contains all binary operations implemented in the programming language.
 */
enum class BinaryOperation {
    PLUS,
    MINUS,
    DIV,
    TIMES,
    REM,
    EQUAL,
    NOT_EQUAL,
    LESS,
    LESS_EQUALS,
    GREATER,
    GREATER_EQUALS,
    LOGICAL_AND,
    LOGICAL_OR
}