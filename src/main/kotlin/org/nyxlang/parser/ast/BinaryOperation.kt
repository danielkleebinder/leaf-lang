package org.nyxlang.parser.ast

/**
 * Contains all binary operations implemented in the programming language.
 */
enum class BinaryOperation {
    PLUS,
    MINUS,
    DIV,
    TIMES,
    REM,
    EQUAL,
    NOT_EQUAL,
    LESS_THAN,
    LESS_THAN_OR_EQUAL,
    GREATER_THAN,
    GREATER_THAN_OR_EQUAL,
    LOGICAL_AND,
    LOGICAL_OR
}